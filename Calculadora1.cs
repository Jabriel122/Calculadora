namespace Calculadora
{
    public class Calculadora1
    {
        public float n1;

        public float n2;

        // Metodos 

        public float Multiplicar ( float n1, float n2)
        {
            return n1*n2;
        }

        public float adição (float n1, float n2)
        {
            return n1+n2;
        }

        public float divisão (float n1, float n2)
        {
            return n1/n2;
        }

        public float subtração (float n1, float n2)
        {
            return n1-n2;
        }
    }
}